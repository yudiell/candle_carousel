    <h2>The Science of Convection</h2>
    
    <p>
        The science behind candle carousels involves convection, which is the transfer of heat through the movement of 
        fluids (liquids or gases). Convection is one of three methods of heat transfer (along with conduction and radiation). 
        In convection, heat moves through the movement of fluids—either liquids or gases.
    </p>
    
    <h3>How Convection Works</h3>
    
    <p>When air is heated:</p>
    <ol>
        <li>The air molecules gain energy and move faster</li>
        <li>The air expands and becomes less dense</li>
        <li>The less dense (warmer) air rises above the denser (cooler) air</li>
        <li>As the warm air rises and moves away, cooler air moves in to replace it</li>
        <li>This creates a continuous circulation called a convection current</li>
    </ol>
    
    <h3>How Convection Creates Motion in Candle Carousels</h3>
    
    <p>
        When a candle burns, it heats the air directly above it. Hot air is less dense than cold air, causing it to rise. 
        This creates a convection current as warm air rises, cools, and falls back down. When the rising hot air encounters 
        the tilted blades of a carousel, it exerts a force on them. Because the blades are angled, this upward force is 
        converted into a sideways push, creating lift and causing the carousel to spin.
    </p>
    
    <p>In a candle carousel specifically:</p>
    <ul>
        <li>The candle flame heats the air directly above it to temperatures significantly higher than room temperature</li>
        <li>This hot air rises rapidly upward</li>
        <li>When the rising air encounters the angled blades, it cannot continue straight upward</li>
        <li>The angle of the blades redirects the upward force into a sideways (horizontal) force</li>
        <li>This sideways force is called "lift" and causes the blades to rotate</li>
        <li>As the blades rotate, they continue to encounter fresh rising hot air, maintaining continuous motion</li>
    </ul>
    
    <h3>Comparison to Wind Turbines</h3>
    
    <p>
        The force exerted by the rising air is similar to how wind turbines work, except that in a candle carousel, the 
        "wind" is created by heat rather than natural air movement. The angle of the blades is crucial—if they are too flat, 
        heat accumulates underneath without rising efficiently; if they are too vertical, the heat escapes too quickly without 
        pushing the blades effectively.
    </p>
    
    <h3>Factors Affecting Efficiency</h3>
    
    <p>The efficiency of this system depends on:</p>
    <ul>
        <li><strong>Blade angle:</strong> 30-40 degrees is optimal for converting vertical air movement to horizontal rotation</li>
        <li><strong>Heat intensity:</strong> More heat creates faster-moving air and greater force</li>
        <li><strong>Blade surface area:</strong> Larger blades catch more air but also add weight</li>
        <li><strong>Weight distribution:</strong> Lighter materials spin more easily</li>
        <li><strong>Air flow patterns:</strong> Interference from room air currents can disrupt the convection system</li>
    </ul>
    
